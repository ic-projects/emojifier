def sample(words, model, tags, emojis, probs, threshold, decay, sim_weight, prob_weight, emojinumber):
    emojistoadd = []
    for i in range(len(words)):
        if (i == 0):
            values = getValue(words[i], model, tags, emojis, probs, sim_weight, prob_weight, threshold)
        else:
            values = [x + y for x, y in zip(map(lambda x: x / decay, values),
                                            getValue(words[i], model, tags, emojis, probs, sim_weight, prob_weight,
                                                     threshold))]
        sortedemo = sorted(zip(emojis, values), key=lambda x: x[1])
        toappend = ""
        for i in range(emojinumber):
            e, v = sortedemo[-(i + 1)]
            if v >= threshold + 3*threshold*i/emojinumber:
                toappend += e
        emojistoadd.append(toappend)

    finalstring = ""
    for i in range(len(words)):
        finalstring += " "
        finalstring += words[i]
        if (not (emojistoadd[i] == '')):
            finalstring += " "
            finalstring += emojistoadd[i]
    return finalstring[1:]


def getValue(lastword, model, tags, emojis, probs, sim_weight, prob_weight, threshold):
    values = map(lambda x: similarity(lastword, x, model, probs, sim_weight, prob_weight), tags)
    if np.max(values) == 0:
        values = map(lambda x: findtag(lastword, x, model, threshold), tags)
    return values


def getsimilarity(lastword, tocheck, model):
    if ((lastword in model.vocab) and (tocheck in model.vocab)):
        return model.similarity(lastword, tocheck)
    else:
        return 0


def similarity(lastword, localtags, model, probs, sim_weight, prob_weight):
    values = map(lambda x: getsimilarity(lastword, x, model), localtags)
    values = map(lambda x: pow(x, sim_weight), values)
    values = [(prob_weight + x) * y for x, y in zip(probs, values)]
    return np.average(values)


def findtag(lastword, localtags, model, threshold):
    if (lastword in localtags):
        return threshold * 2
    else:
        return 0


